// MathJax Configuration
window.MathJax = {
  tex: {
    inlineMath: [['$', '$'], ['\\(', '\\)']],
    displayMath: [['$$', '$$'], ['\\[', '\\]']]
  },
  svg: { fontCache: 'global' }
};

// Load MathJax asynchronously
(function loadMathJax() {
  const script = document.createElement('script');
  script.src = 'https://cdn.jsdelivr.net/npm/mathjax@3/es5/tex-mml-chtml.js';
  script.async = true;
  document.head.appendChild(script);
})();

(async function main() {
  const feedUrls = ['http://rss.arxiv.org/rss/math.RA/'];
  const proxyBase = 'https://api.rss2json.com/v1/api.json?rss_url=';
  const feedContainer = document.getElementById('feed');

  try {
    // 1. Fetch live feeds for the default view
    const responses = await Promise.all(
      feedUrls.map(url => fetch(proxyBase + encodeURIComponent(url)).then(res => res.json()))
    );
    
    let liveItems = responses.flatMap((data, i) => 
      (data.status === 'ok' && Array.isArray(data.items))
        ? data.items.map(item => ({ ...item, __source: feedUrls[i] }))
        : []
    ).sort((a, b) => new Date(b.pubDate) - new Date(a.pubDate));

    if (!liveItems.length) {
      feedContainer.innerHTML = '<p>No items could be loaded from any feed.</p>';
    } else {
      renderItems(liveItems, feedContainer);
    }

    // --- FIX: Make "0ch" Logo redirect home ---
    const logoBtn = document.querySelector('.navbar .container .navbar-brand');
    if (logoBtn) {
      logoBtn.addEventListener('click', (e) => {
        e.preventDefault();
        window.location.reload(); 
      });
    }

    // 2. Setup Archive Button Logic
    const navContainer = document.querySelector('.navbar .container');
    if (navContainer) {
      const archiveBtn = document.createElement('a');
      archiveBtn.href = '#';
      archiveBtn.className = 'navbar-brand'; 
      archiveBtn.textContent = ' [Archive]';
      navContainer.appendChild(archiveBtn);

      let isArchiveView = false;
      let archiveItems = null; 

      archiveBtn.addEventListener('click', async (e) => {
        e.preventDefault();
        isArchiveView = !isArchiveView;
        archiveBtn.textContent = isArchiveView ? ' [Back to Feed]' : ' [Archive]';
        
        feedContainer.innerHTML = ''; 
        
        if (isArchiveView) {
          // --- FIX: Changed <label> to <span> to remove click behavior ---
          feedContainer.innerHTML = `
            <div class="card mb-4">
              <div class="card-body" style="display: flex; gap: 15px; align-items: center; flex-wrap: wrap;">
                <span style="margin: 0; font-weight: bold; font-family: inherit;">Filter by Date:</span>
                <input type="date" id="archive-date" style="padding: 6px 12px; border: 1px solid #ccc; border-radius: 4px; font-family: inherit; color: inherit; flex-grow: 1; max-width: 250px;">
                <button id="clear-date" style="padding: 6px 16px; cursor: pointer; border: 1px solid #ccc; background: transparent; border-radius: 4px; font-family: inherit; color: inherit; -webkit-appearance: none; appearance: none;">Clear Filter</button>
              </div>
            </div>
            <div id="archive-content"></div>
          `;
          
          const contentContainer = document.getElementById('archive-content');
          const dateInput = document.getElementById('archive-date');
          const clearBtn = document.getElementById('clear-date');

          // Fetch the static archive.json generated by GitHub Actions
          if (!archiveItems) {
            contentContainer.innerHTML = '<p>Loading archive...</p>';
            try {
              const res = await fetch('./archive.json');
              archiveItems = await res.json();
            } catch (err) {
              console.error("Could not load archive:", err);
              archiveItems = []; 
            }
          }
          
          // Initial render of all archive items
          renderItems(archiveItems, contentContainer);

          // Filter Logic
          dateInput.addEventListener('change', (event) => {
            const selectedDate = event.target.value; 
            if (!selectedDate) {
              return renderItems(archiveItems, contentContainer);
            }

            const filteredItems = archiveItems.filter(item => {
              const itemDate = new Date(item.pubDate).toISOString().split('T')[0];
              return itemDate === selectedDate;
            });

            if (filteredItems.length === 0) {
              contentContainer.innerHTML = '<p style="padding: 20px;">No articles found for this date.</p>';
            } else {
              renderItems(filteredItems, contentContainer);
            }
          });

          // Clear button logic
          clearBtn.addEventListener('click', () => {
            dateInput.value = '';
            renderItems(archiveItems, contentContainer);
          });

        } else {
          // Show live feeds
          renderItems(liveItems, feedContainer);
        }
      });
    }

  } catch (error) {
    feedContainer.innerHTML = `<p style="color:red">Error loading feeds: ${error.message}</p>`;
    console.error('Feed fetch error:', error);
  }
})();

// Helper function to render items and trigger MathJax
function renderItems(items, container) {
  container.innerHTML = ''; 
  const fragment = document.createDocumentFragment();
  
  // Updated date options: added weekday, removed hour/minute
  const dateOptions = { 
    weekday: 'short',
    year: 'numeric', 
    month: 'short', 
    day: 'numeric'
  };

  items.forEach(item => {
    const article = document.createElement('article');
    article.className = 'card mb-4';
    
    // Changed to toLocaleDateString and used 'en-GB' for Day-Month-Year ordering
    const formattedDate = new Date(item.pubDate).toLocaleDateString('en-GB', dateOptions);

    const authors = item.author ? item.author : 'Unknown Authors';
    const subjects = (item.categories && item.categories.length > 0) 
      ? item.categories.join(', ') 
      : 'No subjects provided';

    article.innerHTML = `
      <header class="card-header">
        <h2 class="card-title">
          <a href="${item.link}" target="_blank" rel="noopener noreferrer">${item.title}</a>
        </h2>
        <time class="card-meta">${formattedDate}</time>
      </header>
      <div class="card-body">
        <p>${item.contentSnippet || ''}</p>
      </div>
      <footer class="card-footer">
        <small><strong>Authors:</strong> ${authors}</small><br>
        <small><strong>Subjects:</strong> ${subjects}</small>
      </footer>
    `;
    fragment.appendChild(article);
  });
  
  container.appendChild(fragment);
  
  if (window.MathJax?.typesetPromise) {
    window.MathJax.typesetPromise().catch(err => console.error(err));
  }
}
